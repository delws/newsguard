"""Витягання атомарних фактичних тверджень (claims) з тексту поста.

Атомарний claim — одне самодостатнє фактичне твердження, зрозуміле без
контексту поста: займенники розгорнуті, відносні дати ("сьогодні", "вчора")
переведені в абсолютні за датою публікації.

НЕ витягуються: оцінки, емоції, заклики, питання, реклама, прогнози-думки.
Валідуються ТВЕРДЖЕННЯ, а не "канал загалом" — тому якість цього кроку
визначає якість усього рейтингу.
"""
from __future__ import annotations

import logging
from datetime import datetime

from . import llm

log = logging.getLogger(__name__)

# Верхня межа тверджень з одного поста: захист від "простирадл" у довгих постах
MAX_CLAIMS_PER_POST = 10

SYSTEM_PROMPT = """Ти — екстрактор фактичних тверджень з новинних постів.

Твоє завдання: розбити текст поста на АТОМАРНІ ФАКТИЧНІ ТВЕРДЖЕННЯ (claims).

Правила:
1. Атомарність: одне твердження = один факт, який можна перевірити. Складне
   речення розбивай на кілька тверджень.
2. Самодостатність: твердження має бути зрозумілим БЕЗ тексту поста.
   Розгорни займенники та скорочення в повні назви. Відносні дати
   ("сьогодні", "вчора", "цієї ночі") переведи в абсолютні, використовуючи
   дату публікації поста.
3. Витягуй ЛИШЕ перевірювані факти: події, цифри, заяви офіційних осіб
   (у формі "X заявив, що Y"), рішення, призначення.
4. НЕ витягуй: оцінки та емоції автора, риторичні питання, заклики,
   рекламу, загальновідомі фонові факти, прогнози й припущення автора.
5. НЕ витягуй мета-твердження про сам канал чи акт публікації: «наші
   кореспонденти повідомляють», «стежте за оновленнями», «деталі за
   посиланням», «ексклюзив нашого видання», анонси трансляцій. Твердження
   має бути про ПОДІЮ У СВІТІ, а не про те, хто і як про неї повідомив.
6. Якщо в пості немає жодного перевірюваного факту — поверни порожній список.
7. Не більше 10 найважливіших тверджень.
8. Мова тверджень — мова поста.

Відповідай ЛИШЕ валідним JSON без пояснень:
{"claims": ["твердження 1", "твердження 2", ...]}"""


def extract_claims(post_text: str, published_at: datetime, cfg: dict) -> list[str]:
    """Повертає список атомарних claims для тексту поста.

    cfg — блок llm.extractor з config.yaml.
    """
    user = (
        f"Дата публікації поста: {published_at:%Y-%m-%d %H:%M} (UTC)\n\n"
        f"Текст поста:\n{post_text}"
    )
    result = llm.complete(SYSTEM_PROMPT, user, cfg)
    claims = result.get("claims")
    if not isinstance(claims, list):
        log.warning("Екстрактор повернув неочікувану структуру: %s", str(result)[:200])
        return []
    clean = [c.strip() for c in claims if isinstance(c, str) and len(c.strip()) >= 15]
    if len(clean) > MAX_CLAIMS_PER_POST:
        clean = clean[:MAX_CLAIMS_PER_POST]
    return clean
